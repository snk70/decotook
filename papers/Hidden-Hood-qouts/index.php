        <h1 dir="RTL" style="text-align: justify;">
	<span style="font-size:20px;">نکاتی در مورد هودهای زیر کابینتی (هود کتابی)</span></h1>
<p dir="RTL" style="text-align: justify;">
	در هنگام خرید هود و به طور کلی کالاهای مربوط به دکوراسیون آشپزخانه، اولویت هایی از جمله شکل ظاهری هود، نحوه نظافت هود و هماهنگی کلی در محیط علاوه بر کیفیت و مشخصات فنی مهم است.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید هود مخفی" class="e-w" src="http://decotook.com/papers/files/pictures/Hidden-Hood-qouts/Hidden-Hood-qouts-1.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	در این بخش به راهنمایی های مفید درباره هود مخفی آشپزخانه توجه کنید:</p>
<p dir="RTL" style="text-align: justify;">
	هودهای مخفی، درون کابینت نصب می شوند و صفحه مکش آن ها، با قدرت مکش بالا، کاملاً بر روی اجاق گاز قرار می گیرد. این هودها به دلیل اشغال فضای کمتر، برای نصب در آشپزخانه هایی با ابعاد کوچک مناسب است. هودهای مخفی معمولا در سایزی عرضه می شود که مناسب قرار گیری بالای کلیه اجاق گازهای موجود در بازار است.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید هود مخفی" class="e-w" src="http://decotook.com/papers/files/pictures/Hidden-Hood-qouts/Hidden-Hood-qouts-2.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	میزان صدای این هود به دلیل قرار گیری موتور در یک محفظه فلزی و نصب آن داخل کابینت معمولا کم است و این مزیت موجب می شود با آرامش بیشتری بتوانید به امور منزل بپردازید.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید هود مخفی" class="e-w" src="http://decotook.com/papers/files/pictures/Hidden-Hood-qouts/Hidden-Hood-qouts-3.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h2 dir="RTL" style="text-align: justify;">
	نکات کلیدی که هنگام نصب هودهای زیرکابینتی (کتابی) آشپزخانه باید به آنها توجه کنید:</h2>
<p dir="RTL" style="text-align: justify;">
	نصب صحیح هود آشپزخانه در عمر مفید هود و مکش آن مؤثر است و باعث جلوگیری از ایجاد هزینه های اضافی می شود. بنابراین پیش از نصب هود به موارد زیر توجه فرمائید:</p>
<p dir="RTL" style="text-align: justify;">
	1- در هنگام خرید هود از سالم بودن کالا مطمئن شوید.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید هود مخفی" class="e-w" src="http://decotook.com/papers/files/pictures/Hidden-Hood-qouts/Hidden-Hood-qouts-4.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	2- هود توسط افراد دارای صلاحیت و نماینده خدمات پس از فروش مجاز شرکت نصب شود.</p>
<p dir="RTL" style="text-align: justify;">
	3- هود در ناحیه پخت غذا باید با فاصله 65 الی 80 سانتیمتر از اجاق گاز نصب شود.</p>
<p dir="RTL" style="text-align: justify;">
	4- اندازه هود باید طوری انتخاب شود که تمام سطح گاز را بپوشاند.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید هود مخفی" class="e-w" src="http://decotook.com/papers/files/pictures/Hidden-Hood-qouts/Hidden-Hood-qouts-5.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	5- در صورت امکان، هودهای مدل زیر کابینتی از قسمت بالا به کابینت متصل شوند.</p>
<p dir="RTL" style="text-align: justify;">
	6- کوتاه ترین مسیر را با کمترین زاویه برای خروج هوا انتخاب نمائید.</p>
<p dir="RTL" style="text-align: justify;">
	7- گرد و خاک و آلودگی قسمتهای قابل دسترس را تمیز کنید.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید هود مخفی" class="e-w" src="http://decotook.com/papers/files/pictures/Hidden-Hood-qouts/Hidden-Hood-qouts-6.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	8- درصورت کوتاه بودن طول سیم تا حد امکان از رابط استفاده کنید و از افزودن به طول سیم جدا خودداری کنید.</p>
<p dir="RTL" style="text-align: justify;">
	9- &nbsp;لوله خروجی هوا را هیچگاه به خروجی هواکش، خروجی شومینه، خروجی طبقات دیگر، لوله بخاری و لوله آبگرمکن متصل نکنید.</p>
<p dir="RTL" style="text-align: justify;">
	10- درمحل هائی که خروجی هوا به خارج ساختمان وجود داشته باشد، هوای نامطبوع از طریق لوله خروجی هوا به خروجی هوا به خارج ساختمان هدایت می شود. در این حالت خروجی هوا باید باز باشد و محفظه پشت هود مسدود شده باشد.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
