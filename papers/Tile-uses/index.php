<p dir="RTL" style="text-align: center;">
	<img alt="دستشویی - توالت فرنگی" class="e-w" src="http://decotook.com/papers/files/pictures/Toilet-suitble-choice/Toilet-suitble-choice-9.jpg" /></p>
