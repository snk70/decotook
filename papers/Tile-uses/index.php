<p dir="RTL" style="text-align: justify;">
	در گذشته تعدد رنگ در چینی آلات بهداشتی بیشتر وجود داشت. توصیه می شود کاسه توالت ایرانی و حتی فرنگی از رنگ های خیلی روشن و ترجیحاً رنگ <strong>سفید </strong>انتخاب شوند. زیرا وجود خون در ادرار یا مدفوع و یا وجود برخی انگل های عفونی قابل رویت در مدفوع نشانه انواع بیماری های انگلی، عفونی، آلودگی، انواع سرطان ها، زخم های روده، معده و یا پلیب های روده و غیره خواهد بود.</p>
