<p dir="RTL" style="text-align: justify;">
	۱- ارتفاع سقف :</p>
<p dir="RTL" style="text-align: justify;">
	اگر سقفتان کوتاه است نباید از لوستر های آویز استفاده کنید زیرا سقف شما را کوتاه تر از اندازه واقعی نشان می دهد. بهتر است از لوسترهای سقفی استفاده کنید، ولی اگر سقف شما بلند است بهتر آن است که از لوستر هایی استفاده کنید که ارتفاع بیشتری دارند تا لوستر شما جلب توجه نموده و در فضا گم نشود.</p>
<p dir="RTL" style="text-align: center;">
