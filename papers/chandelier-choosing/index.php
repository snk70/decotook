<p dir="RTL" style="text-align: justify;">
	۳- محل استفاده وکاربرد:</p>
<p dir="RTL" style="text-align: justify;">
	اگر لوستر را برای سالن و پذیرایی خود می خواهید طیف وسیعی از محصولات را می توانید انتخاب نمایید. (لوستر های ال ای دی،کریستالی،شاخه ای و&hellip;)</p>
<p dir="RTL" style="text-align: center;">
