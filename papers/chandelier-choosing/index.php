	۵- مقدار نور مورد نیاز :</p>
<p dir="RTL" style="text-align: justify;">
	با توجه به افزایش بهای برق مصرفی،در دکوراسیون های جدید سعی می شود از نورهای مخفی و چراغ های ال ای دی سقفی که از مصرف انرژی پایین تری برخوردارند، به عنوان منبع اصلی نور استفاده شود و لوستر فقط جنبه زیبایی داشته و معمولا به هنگام مراسم و مهمانی ها از آن استفاده شود.</p>
<p dir="RTL" style="text-align: justify;">
	حال اگر تنها منبع نور شما لوستر است، باید دقت عمل بیشتری به خرج داده و محصولی را انتخاب نمایید که نور شما را به طور کامل تامین نماید و در عین حال از نظر ابعاد با دکوراسیون شما هماهنگ باشد، زیرا اگر لوستر شما برای فضای منزل شما بزرگ باشد، تعادل محل شما را به هم خواهد زد و اگر کوچک باشد در فضای شما کمرنگ دیده می شود. پس اگر فضای کوچکی دارید و نیاز به نور زیاد از لوستری استفاده کنید که از ابعاد کوچکتر و تعداد لامپ بیشتری بهره ببرد و نه یک لوستر بزرگتر.</p>
