<p dir="RTL" style="margin-left: 21.3pt; text-align: justify;">
	&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; اسباب بازی ها، قاب عکس ها و موارد اضافی که باعث حواس پرتی و کاهش تمرکز می شود را از اتاق مطالعه فرزندتان خارج کنید.</p>
<p dir="RTL" style="margin-left: 21.3pt; text-align: justify;">
	&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; به هیچ عنوان تلوزیون را در مکان مطالعه فرزندتان قرار ندهید. بررسی&zwnj;ها نشان دادن داده&zwnj;اند قرار دادن تلویزیون در اتاق کودکان و نوجوانان موجب کاهش تمرکز در آنها می شود.</p>
<p dir="RTL" style="margin-left: 21.3pt; text-align: justify;">
