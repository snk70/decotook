<p dir="RTL" style="text-align: justify;">
	<strong>نور پردازی مناسب در دکوراسیون اتاق مطالعه</strong></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="اتاق مطالعه فرزندان" class="e-w" src="http://decotook.com/papers/files/pictures/children's-study-room/children's-study-room-2.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	نور اتاق یکی از عوامل مهمی است که باید در طراحی دکوراسیون اتاق مطالعه در نظر بگیریم. در نظر داشته باشید اتاقی را برای این کار انتخاب کنید که دارای پنجره و نور طبیعی باشد. کمبود نور مناسب به ویژه نور طبیعی در روز باعث خواهد شد که خستگی ناشی از درس خواندن افزایش یابد و احساس خواب آلودگی و هواس پرتی درفرزندتان ایجاد شود. نور مناسب یکی از عوامل موثر در افزایش تمرکز است. سعی کنید در روز تا حد امکان با استفاده از پرده هایی که نور را از خود عبور می دهند و یا کنار زدن پرده ها از نور طبیعی بهره ببرید و هنگام تاریک شدن هوا از لامپ های مناسب، چراغها وچراغ مطالعه استفاده کنید.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="اتاق مطالعه فرزندان" class="e-w" src="http://decotook.com/papers/files/pictures/children's-study-room/children's-study-room-3.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	برای نورپردازی اتاق مطالعه از ترکیب نورهای زرد و سفید در اتاق استفاده کنید. ترکیب نور زرد وسفید باعث افزایش تمرکز و یادگیری می&zwnj;شود. از چراغ&zwnj; مطالعه&zwnj;های پایه&zwnj; بلند استفاده کنید. این چراغ ها نور را از فاصله دورتری می تابانند و باعث تاری دید نمی شوند. &nbsp;بهتر است لامپ چراغ &zwnj;مطالعه از نوع لامپ&zwnj;های رشته&zwnj;ای باشد. نور سفید لامپ&zwnj;های فلورسنت برای مطالعه مناسب نیست. همچنین هنگام استفاده از چراغ مطالعه نور باید از سمت چپ بتابد تا سایه دست به سمت راست بیفتد و مزاحم نوشتن و مطالعه نشود. &nbsp;برای افرادی که با دست چپ می&zwnj;نویسند نیز&nbsp; نور باید از سمت راست بتابد.</p>
<p dir="RTL" style="text-align: justify;">
	از لوسترهایی که حباب<span dir="LTR">&lrm;</span>های رنگی یا قاب تیره دارند، استفاده نکنید زیرا روشنایی را کاهش داده و نور رنگی ایجاد می<span dir="LTR">&lrm;</span>کنند. برای پیشگیری از خستگی چشم ازلامپ هایی با قاب سفید و مات برای اتاق مطالعه استفاده کنید.</p>
<p dir="RTL" style="text-align: justify;">
	در آخر، اگر فرزندتان از کامپیوتر برای مطالعه استفاده می کند، کامپیوتر را جلوی پنجره قرار ندهید تا انعکاس نور چشم او را آزار ندهد.</p>
<p dir="RTL" style="text-align: justify;">
	<strong>انتخاب میز تحریر و صندلی مناسب</strong></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="اتاق مطالعه فرزندان" class="e-w" src="http://decotook.com/papers/files/pictures/children's-study-room/children's-study-room-4.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	یکی از وسایل مهم&zwnj; در اتاق مطالعه&nbsp; میز و صندلی مناسب برای مطالعه است. صندلی استاندارد صندلی است که به قسمت&zwnj;های مختلف بدن مانند کمر و گردن وستون فقرات فشار نیاورد وفرزندتان بتواند ساعت&zwnj;ها پشت میزش راحت بنشیند و درس بخواند. درانتخاب صندلی به جثه فرزندتان توجه کنید و صندلی را متناسب با جثه او انتخاب نمایید. سن و قد او را نیز در نظر بگرید. صندلی را انتخاب کنید که تکیه گاه محکم و قابلیت چرخش داشته باشد. برای انتخاب میز تحریر نیز در نظر داشته باشید که مناسب ترین ارتفاع برای میز تحریر، ارتفاع ۷۰ تا ۸۰ سانتی متر است. اگر فرزندتان باید از رایانه برای انجام تکالیفش استفاده کند، میزی را تهیه کنید که قابلیت قرار دادن رایانه را هم داشته باشد و بتوان از آن به عنوان میز تحریر نیز استفاده کرد.</p>
<p dir="RTL" style="text-align: justify;">
	<strong>رنگ های مناسب برای اتاق مطالعه</strong></p>
<p dir="RTL" style="text-align: justify;">
