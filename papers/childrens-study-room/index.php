<p dir="RTL" style="text-align: center;">
	<img alt="اتاق مطالعه فرزندان" class="e-w" src="http://decotook.com/papers/files/pictures/children's-study-room/children's-study-room-5.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	برای انتخاب رنگ مناسب اتاق مطالعه فرزندتان ابتدا باید روحیه و علاقه او رادر نظر بگرید. سعی کنید از رنگ های گرم وسرد در کنار هم استفاده کنید. رنگ های گرم سبب شور و نشاط در فرزندتان می شود اما استفاد بیش از حد آن نیز توصیه نمی شود. رنگ های سرد باعث کم شدن استرس و آرامش فرزندتان می شود اما استفاده مطلق از این رنگ ها باعث خستگی و کسالت او می شوند. پس سعی کنید متناسب باروحیه فرزندتان از این دو دسته رنگ در زمینه ای از رنگ های خنثی استفاده نمایید. از رنگ های مناسب برای اتاق مطالعه ترکیب رنگ سبز و زرد است این دو رنگ باعث افزایش تمرکز دردانش آموزان می شود. به طور کلی رنگ زرد، سفید، سبز، وطیف هایی از قهوه ای کم رنگ رنگ هایی هستند که باعث بالا رفتن سطح تمرکز می شود.</p>
<p dir="RTL" style="text-align: justify;">
	<strong>نکته های کاربردی در دکوراسیون اتاق مطالعه</strong></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="اتاق مطالعه فرزندان" class="e-w" src="http://decotook.com/papers/files/pictures/children's-study-room/children's-study-room-6.jpg" /></p>
