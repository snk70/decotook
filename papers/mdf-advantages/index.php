<p dir="RTL" style="text-align: justify;">
	<strong>2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;به راحتی تمیز می شوند.</strong><br />
	از آنجایی که سطح کابینت های ام دی اف<span dir="LTR"> (MDF) </span>از لایه نازک وینیل تشکیل شده است، به راحتی تمیز می شوند. بیشتر لکه ها را می توانید با اسفنج یا یک تکه پارچه نرم و آب پاک کنید. برای لکه های مقاوم تر، می توانید به اسفنج تان کمی شوینده خانگی غیر ساینده اضافه کنید. با وجود این، از به کار بردن شوینده های قوی که حاوی مواد نفتی، الکلی یا استون هستند، خودداری کنید زیرا ممکن است به سطح کابینت آشپزخانه آسیب برسانند. پس از پاک کردن لکه، کابینت را با دستمال کاغذی خشک نمایید تا از آسیب های ناشی از رطوبت جلوگیری شود.</p>
