<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>زیرسازی فلزی کناف</strong></h3>
<p dir="RTL" style="text-align: justify;">
	پروفیل های کناف انواع متفاوتی دارد که با شکل دادن به ورق های گالوانیزه فولادی به روش نورد سرد تولید می شوند. پیچ ها ، سازه درز انقطاع ، نوار درزگیر و ماستیک و&hellip; از دیگر اجزای دیوار کناف می باشد.</p>
<p dir="RTL" style="text-align: justify;">
	در مناطقی که نیاز به ساخت و ساز سریع باشد مانند مناطق زلزله زده، استفاده از سیستم ساخت و ساز خشک یا کناف در اولویت است.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="سازه های کناف" class="e-w" src="http://decotook.com/papers/files/pictures/plasterboard-or-gypsum/plasterboard-or-gypsum-7.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	در ادامه مراحل اجرای سقف کاذب را برای نمونه در دو سیستم بنایی و سیستم ساخت و ساز خشک شرح خواهیم داد:</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
