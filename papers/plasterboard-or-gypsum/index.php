        <h1 dir="RTL" style="text-align: justify;">
	<strong>کناف چیست؟</strong></h1>
<p dir="RTL" style="text-align: justify;">
	اگر شما هم جزو کسانی هستید که اولین بار اسم کناف به گوشتان خورده است؛ و یا با نحوه و مراحل اجرای آن آشنایی کامل ندارید حتماً مطلب زیر را مطالعه نمایید.</p>
<p dir="RTL" style="text-align: justify;">
	یکی از ابتدایی ترین و متداول ترین سوالها در مرحله ی اجرای سقف کاذب برای افرادی که منزل خود را بازسازی می کنند و حتی سازندگان با تجربه این پرسش است که کناف بهتر است یا اندود گچ و رابیتس؟ در پاسخ به این سوال لازم است بدانیم نحوه ی اجرای هر کدام چگونه است تا بتوانیم مقایسه منصفانه ای بین دو سیستم داشته باشیم. مقایسه ی هر کدام از آنها را از دو جنبه ی شکل و محتوا و معایب و مشکلات اجرایی هر کدام را در مطلب بعد مورد بررسی قرار خواهیم داد.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="سقف کاذب کناف" class="e-w" src="http://decotook.com/papers/files/pictures/plasterboard-or-gypsum/plasterboard-or-gypsum-1.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<h2 dir="RTL" style="text-align: justify;">
	<strong>آشنایی کلی با کناف</strong></h2>
<p dir="RTL" style="text-align: justify;">
	پیشرفت فناوری های ساختمان در سال های اخیر جایگزینی مصالح نوین را بجای مصالح سنتی سبب شده است.</p>
<p dir="RTL" style="text-align: justify;">
	از جمله این موارد سیستم های ساخت و ساز خشک است. این صنعت برای اولین بار توسط شرکت کناف ایران با برند کناف در سال 78 در ایران عرضه شد. به همین دلیل ساخت و ساز خشک با نام کناف شهرت یافته است. استفاده از این تکنولوژی در اغلب کشورهای اروپایی و حتی در ایران در حال گسترش است. تخته های گچی که پنل گچی کناف نام دارد یکی از اصلی ترین مصالح مورد استفاده در ساخت و ساز خشک است<span dir="LTR">.</span></p>
<p dir="RTL" style="text-align: justify;">
	کناف نوعی صفحه روکش دار گچی<span dir="LTR"> (Dry Wall) </span>&nbsp;است که توسط شرکت کناف تولید می شود. شرکت کناف در آلمان بیش از 8۰ سال است که در صنایع ساختمانی فعالیت دارد. این شرکت توسط برادران آرل کناف و آنفوس کناف در سال ۱۹۳۲ بنیان گزاری شد و در حال حاضر یکی از شرکت های بین المللی در صنایع ساختمان است.</p>
<p dir="RTL" style="text-align: justify;">
	کناف دارای چندین سیستم ساخت و ساز است از جمله می توان به سیستم های سقف کاذب کناف و سیستم های دیوار جداکننده و دیوار پوششی کناف اشاره کرد.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="سقف کاذب کناف" class="e-w" src="http://decotook.com/papers/files/pictures/plasterboard-or-gypsum/plasterboard-or-gypsum-2.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	کارل و آنفوس کناف - موسسین شرکت کناف آلمان</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="ایزابل کناف" class="e-w" src="http://decotook.com/papers/files/pictures/plasterboard-or-gypsum/plasterboard-or-gypsum-3.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	خانم ایزابل کناف - عضو هیئت مدیره شرکت کناف آلمان</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<h2 dir="RTL" style="text-align: justify;">
	<strong>چرا از کناف استفاده می کنم؟</strong></h2>
<p dir="RTL" style="text-align: justify;">
	در ادامه به برخی از مزایای دیوار کناف اشاره خواهد شد:</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>کاهش بار مرده ساختمان</strong></h3>
<p dir="RTL" style="text-align: justify;">
	این نوع دیوار در مقایسه با دیوارهای بنایی ده تا پانزده برابر سبک تر است. بنابراین می تواند با کاهش بار مرده ساختمان، تأثیر نیروی زلزله را بر ساختمان کمتر کند. علاوه بر این در صورت تخریب ساختمان، به علت سبکی این نوع مصالح، خطر جانی کمتری برای ساکنان ساختمان خواهدداشت.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>نحوه اتصال انعطاف پذیر پانل های گچی</strong></h3>
<p dir="RTL" style="text-align: justify;">
	سبک بودن مصالح کناف، نحوه اتصال انعطاف پذیر پانل های گچی به زیرسازی فلزی و پروفیل ها، همگی به گونه ای هستند که نیروی زلزله تأثیر مخرب کمتری بر ساختمان خواهد داشت.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>حمل آسان و افزایش سرعت اجرا</strong></h3>
<p dir="RTL" style="text-align: justify;">
	همچنین سبک بودن مصالح کناف موجب حمل آسان و افزایش سرعت اجرای پروژه خواهد شد. در مسافت های طولانی با بسته بندی مناسب، می توان از میزان آسیب به مصالح جلوگیری کرد. حجم کم مصالح کناف بدلیل بسته بندی قالبی کاهش هزینه های حمل را نسبت به مصالح سنتی در بر خواهد&nbsp; داشت.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="حمل مصالح کناف" class="e-w" src="http://decotook.com/papers/files/pictures/plasterboard-or-gypsum/plasterboard-or-gypsum-4.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>دسترسی آسان به تاسیسات مکانیکی و الکتریکی</strong></h3>
<p dir="RTL" style="text-align: justify;">
	از مزایای دیگر سیستم کناف دسترسی آسان به تاسیسات مکانیکی و الکتریکی است. در صورت ترکیدن لوله های تاسیسات و&hellip; به راحتی می توان قسمت آسیب دیده را تشخیص داده و تعمیر کرد. در صورت آسیب دیدن دیوارهای کناف در اثر ضربه نیز، قسمت آسیب دیده به راحتی تعویض و تعمیر می شوند. این دو ویژگی موجب افزایش عمر مفید ساختمان می شوند<span dir="LTR">.</span></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="دسترسی آسان به تاسیسات در سیستم کناف" class="e-w" src="http://decotook.com/papers/files/pictures/plasterboard-or-gypsum/plasterboard-or-gypsum-5.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>حذف صرف زمان برای خشک شدن مصالح</strong></h3>
<p dir="RTL" style="text-align: justify;">
	مصالح بنایی نیاز به صرف زمان برای خشک شدن دارند. در مناطق سردسیر و فصول سرد سال لازم است محیط گرم باشد زیرا در صورتی که گچ تازه در مدت معین خشک نشود در مقابل رطوبت خود طبله کرده و به صورت جدا از هم ظاهر می&zwnj;شوند و پس از آنکه رطوبت از بین رفت و محل خشک شد گچ طبله شده به حالت اولیه باز نمی&zwnj;گردد. از این رو می توان گفت سیستم کناف نیازی به صرف زمان برای خشک شدن ندارد و در فصول سرد مشکلات اجرایی کمتری دارد.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>قابلیت رنگ آمیزی بلافاصله پس از نصب</strong></h3>
<p dir="RTL" style="text-align: justify;">
	همچنین دیوارهای کناف بلافاصله بعد از نصب، قابل رنگ آمیزی و رنگ پذیری هستند. این موضوع نیز باعث صرفه جویی و بالا رفتن سرعت اجرای پروژه ها می شود.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>جلوگیری از ایجاد ترک در دیوار</strong></h3>
<p dir="RTL" style="text-align: justify;">
	استفاده از نوار درزگیر موجب استحکام محل درز و جلوگیری از ایجاد ترک در دیوار می شود.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<h3 dir="RTL" style="text-align: justify;">
	<strong>مقاومت در برابر عبور صدا</strong></h3>
<p dir="RTL" style="text-align: justify;">
	یکی از مشکلات ساختمان های نوساز عایق نبودن صدا و حرارت است که موجب مزاحمت واحدهای مجاور در آپارتمان ها است. با به کارگیری دیوارهای کناف می توان مقاومت در برابر عبور صدا را افزایش داده و با استفاده از عایق های پشم سنگ و پلی استایرن دیوارها را در برابر حرارت به راحتی عایق کرد. عایق ها در بین دو پانل گچی قرار می گیرند.</p>
<p dir="RTL" style="text-align: justify;">
	از سیستم پانل های گچی کناف بدون سازه نیز استفاده می شود. در این حالت، پانل های گچی با استفاده از چسب پرلفیکس به عنوان پوشش روی دیوارهای سنتی نصب می شوند. بدین ترتیب فضایی خالی در پشت آن برای عایق کاری و عبور تاسیسات ایجاد می شود<span dir="LTR">.</span></p>
<p dir="RTL" style="text-align: justify;">
	از دیگر مزایای سیستم کناف اجرای فرم های دکوراتیو و منحنی و افزایش سطح زیر بنای ساختمان با توجه به کاهش ضخامت دیوارهای کناف نسبت به مصالح سنتی می باشد<span dir="LTR">.</span></p>
<h2 dir="RTL" style="text-align: justify;">
	&nbsp;</h2>
<h2 dir="RTL" style="text-align: justify;">
	<strong>انواع پنل های کناف</strong></h2>
<p dir="RTL" style="text-align: justify;">
	پنل های گچی کناف انواع متفاوتی متناسب با شرایط محیطی دارد. که به 4 دسته تقسیم می شوند:</p>
<p dir="RTL" style="text-align: justify;">
	در شرایط عمومی از پنل های گچی ساده یا<span dir="LTR"> Regular board (RG)&nbsp; </span>استفاده می شود.</p>
<p dir="RTL" style="text-align: justify;">
	برای مکان های مرطوب مانند سرویس حمام از پنل های مقاوم در برابر رطوبت <span dir="LTR">Moisture Resistant (MR) </span>استفاده می شود.</p>
<p dir="RTL" style="text-align: justify;">
	این صفحات قابل استفاده در مکان هایی با رطوبت کمتر از 90% می باشد.</p>
<p dir="RTL" style="text-align: justify;">
	در محل هایی که احتیاج به محافظت در برابر حریق دارند، پانل هایی &nbsp;<span dir="LTR">Fire Resistant board (FR)</span> که دارای الیاف شیشه می باشند پیشنهاد می گردد که تا 3 ساعت در برابر آتش سوزی مقاومت می کنند.</p>
<p dir="RTL" style="text-align: justify;">
	پانل هایی <span dir="LTR">Fire &amp; Moisture resistant board (FM) </span>هم که در برابر رطوبت و آتش مقاوم هستند، برای مکان هایی مانند چاه های تاسیساتی استفاده می شوند.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
