<p dir="RTL" style="text-align: center;">
	<img alt="درب ضد سرقت" class="e-w" src="http://decotook.com/papers/files/pictures/security-doors-equipment/security-doors-equipment-3.jpg" /></p>
<h2 dir="RTL" style="text-align: justify;">
	<span style="font-size:14px;"><strong>قفل هوشمند</strong></span></h2>
<p dir="RTL" style="text-align: justify;">
	می توان برای برخورداری از ضریب امنیت بیشتر از <strong>کارت های کُد دار</strong> نیز استفاده کرد.</p>
<p dir="RTL" style="text-align: center;">
