<h2 dir="RTL" style="text-align: justify;">
	<span style="font-size:14px;"><strong>درب ضد سرقت دولنگه</strong></span></h2>
<p dir="RTL" style="text-align: justify;">
	درب ضد سرقت دو لنگه دارای ساختار فلزی و نوع چوب <strong>ام دی اف و </strong>روکش <strong>پی وی سی </strong>است. یکی از معیارهایی که برای درب ضد سرقت دو لنگه مهم است قدرت مقاومت درب در قسمت عرض فوقانی آن است. عامل مؤثر در مقاومت این درب، همان لنگه های درب هستند.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="درب ضد سرقت" class="e-w" src="http://decotook.com/papers/files/pictures/security-doors-equipment/security-doors-equipment-7.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	لنگه درب از جنس فلز ساخته شده است. دور تا دور لنگه درب نیز از قوطی فلزی ساخته شده است که موجب افزایش مقاومت درب شده است. بهتر است درب ضد سرقت دولنگه دارای آستانه فلزی نباشد و از همان جنس موادی که در کف زمین استفاده شده است برای آستانه نیز استفاده شود. در این صورت مقاومت قسمت پایینی درب افزایش می یابد. البته استفاده از قفل های مولتی سیستم چهارده زبانه خوب است و قابلیت ضد سرقت بودن درب های دولنگه را در عرض بالا تقویت می کند. استفاده از پیم های گاوصندوقی در پشت هر دو لنگه نیز می تواند در مقاومت درب مؤثر باشد. بهتر است درب های ضد سرقت دولنگه دارای طرح هایی باشند که هر دو لنگه بتوانند مکمل هم باشند.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="درب ضد سرقت" class="e-w" src="http://decotook.com/papers/files/pictures/security-doors-equipment/security-doors-equipment-8.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	توصیه می شود از طرح های دو قاب و سه قاب استفاده شود.</p>
<p dir="RTL" style="text-align: justify;">
	بدیهی است امنیت درب های دولنگه نسبت به درب های تک لنگه تا حدودی به دلیل پهنای بالای آنها کمتر است. رگلاژ درب دولنگه در محل تولید و نیز در محل نصب مشکل تر است زیرا یک طرف درب بر روی لنگه درب می نشیند. این امر موجب کاهش مقاومت آن می شود. اگر در درب های دولنگه از لولاهای ترکیه ای استفاده شود بهتر است زیرا احتمال افتادگی درب به مرور زمان و خارج شدن آن از رگلاژ بیشتر از درب های معمولی است.</p>
