<h1 dir="RTL" style="text-align: justify;">
	<span style="font-size:14px;"><strong>درب ضد سرقت</strong></span></h1>
<p dir="RTL" style="text-align: justify;">
	جدید ترین تکنولوژی برای حفظ ایمنی و امنیت ساختمان ها است که برای پیشگیری از سرقت ابداع و ابتکار شده است. این درب ها در مقابل تجاوز سارقین از جهات مختلفی در مقابل آسیب هایی نظیر برش، اهرم کردن، فشارهای قوی و باز کردن قفل در حد نسبتاً مطلوبی مقاوم هستند و از ضریب ایمنی بالایی برخوردارند. اما نباید چنین پنداشت که این ایمنی صد در صد و کاملاً مطلق است. تجربیات موجود نشان داده اند که این ابتکار موفق بوده و از ضریب ایمنی مطلوبی برخوردارند و با درب های معمولی بسیار متفاوت هستند.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="درب ضد سرقت" class="e-w" src="http://decotook.com/papers/files/pictures/security-doors-equipment/security-doors-equipment-1.jpg" /></p>
<h2 dir="RTL" style="text-align: justify;">
	<span style="font-size:14px;"><strong>تفاوت درب های ضد سرقت با درب های معمولی</strong></span></h2>
<p dir="RTL" style="text-align: justify;">
	<strong>ضخامت چهار چوب</strong> درب ضد سرقت حدود بیش از بیست میلیمتر است و فضای داخلی آن با پشم سنگ پر و عایق شده است.</p>
<p dir="RTL" style="text-align: justify;">
	<strong>لولاهای درب</strong> ضد سرقت معمولاً پنهان هستند و به همین دلیل با روش متداول اهرم کردن&nbsp; نمیتوان آنها را باز کرد.</p>
<p dir="RTL" style="text-align: justify;">
	<strong>پوشش های درب</strong> ضد سرقت متشکل از اجناس و مواد شیشه، <strong>ام. دی. اف</strong>، و یا <strong>اچ. دی.اف</strong>، صفحات فلزی و سایر مواد مقاوم هستند.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="درب ضد سرقت" class="e-w" src="http://decotook.com/papers/files/pictures/security-doors-equipment/security-doors-equipment-2.jpg" /></p>
<h2 dir="RTL" style="text-align: justify;">
	<span style="font-size:14px;"><strong>قفل</strong></span></h2>
<p dir="RTL" style="text-align: justify;">
