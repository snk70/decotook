<p dir="RTL" style="text-align: justify;">
	لازم است قبل از اقدام به خرید پارکت لمینت اطلاعات جامعی در مورد ساختار و نحوه نصب آن داشته باشید. دانستن این نکات برای داشتن یک خرید خوب بسیار حائز اهمیت است. در ادامه گروه دکوراسیون داخلی دکوتوک تجربیات خود را در این زمینه با شما به اشتراک خواهد گذاشت.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	<strong>ابعاد و ویژگی های ظاهری لمینت</strong></p>
<p dir="RTL" style="text-align: justify;">
	شکل کلی</p>
<p dir="RTL" style="text-align: justify;">
	لمینت ها به صورت تایل و با ابعاد مستطیل شکل هستند. لمینت ها با ابعاد مختلفی در بازار موجودند. ضخامت لمینت ها معمولاً 8 میلیمتر می باشد.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-1.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	کلیک لمینت</p>
<p dir="RTL" style="text-align: justify;">
	دور تا دور هر تایل لمینت شیارهایی وجود دارد که در اصطلاح به&nbsp; آنها کلیک می گویند. کلیک ها در هنگام نصب به هم چفت و بست می شوند. کلیک ها خود دارای انواع مختلفی هستند. شرکتهایی در سطح دنیا وجود دارند که کار و فعالیتشان به طور تخصصی طراحی و ساخت کلیک&nbsp; می باشد. چیزی که شما به هنگام خرید یک پارکت لمینت باید به آن توجه داشته باشید، اندازه کلیک است. توجه نمائید که بیرون زدگی کلیک و شیار پارکت لمینت نباید کوچک باشد. اگرچه بزرگ بودن کلیک و شیار لمینت دلیل بر خوب بودن و استحکام آن نیست. اما کوچک بودن کلیک لمینت حتما می تواند دلیل خوبی باشد تا بدانید که کالا از کیفیت پائینی برخوردار است<span dir="LTR">.</span></p>
<p dir="RTL" style="text-align: justify;">
	نمایی از کلیک یک لمینت</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-2.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	نر و مادگی کلیک یک لمینت:</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-3.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	نحوه کلیک کردن دو لمینت در یکدیگر</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-4.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	لمینت چیست؟</p>
<p dir="RTL" style="text-align: justify;">
	لمینیت (<span dir="LTR">laminate</span>) به معنای لایه است و هر چیزی که نقش پوشش داشته باشد را می توان نوعی لمینیت به حساب آورد. در دکوراسیون داخلی نوعی پوشش برای کف از جنس اچ دی اف <span dir="LTR">HDF</span> (<em><span dir="LTR">high-density fiberboard</span></em>) است که خود از چند لایه تشکیل شده است:</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	لایه های مختلف لمینت</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-5.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	تفاوت های پارکت با لیمنت</p>
<p dir="RTL" style="text-align: justify;">
	عمده ترین تفاوت آنها این است که پارکت از چوب واقعی، و لمینیت ترکیبی از مواد چوبی و لایه های محافظ می باشد، همانطور که می دانید پارکت به علت طبیعی بودن، نحوه ی نصب پر دردسر و نیاز به تعمیر و نگهداری بالا و فقدان نیروی ماهر بسیار گران قیمت و پر دردسر است. زیرا پارکت در فضای دکوراسیون خانه حتی با یک لیوان آب نیز متورم شده و از کف جدا می شود.</p>
<p dir="RTL" style="text-align: justify;">
	در اکثر شرکت ها و فروشگاه های کشور، محصولی که به نام پارکت یا پارکت لمینت ارائه می شود، همان لمینیت است.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-6.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	نمایی از یک پارکت:</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-7.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-8.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	نمایی از یک لمینت:</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-9.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-10.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	نقش فوم و زیرسازی در کیفیت نصب لمینت</p>
<p dir="RTL" style="text-align: justify;">
	فوم نقش تعیین کننده در جلوگیری از به سروصدا افتادن پارکت ها و ایجاد مشکلات بعدی دارد. فوم علاوه بر ایجاد انعطاف خوشایند در زیر لمینت در عایق کردن صدا، حرارت و برودت نیز تأثیر بسزایی دارد. ضخامت استاندارد برای فوم مورد استفاده در زیر لمینت 2 میلیمتر است. فوم ها در دو نوع ساده (فوم معمولی) و فشرده (فوم سایلنت) در بازار موجود است.</p>
<p dir="RTL" style="text-align: justify;">
	فوم سایلنت</p>
<p dir="RTL" style="text-align: justify;">
	فوم معمولی:</p>
<p dir="RTL" style="text-align: justify;">
	فومی که برای نصب لمینت مورد نیاز است فوم سایلنت است.</p>
<p dir="RTL" style="text-align: justify;">
	نحوه تشخیص فوم سایلنت از فوم معمولی</p>
<p dir="RTL" style="text-align: justify;">
	از نظر ظاهری فوم معمولی با سایلنت کاملا متفاوت هستند، فوم معمولی حالت برفکی و نایلون گونه می باشد اما فوم سایلنت یکدست و بدون روزنه می باشد،&nbsp;<a href="http://choobineh.net/blog/%d9%87%d9%85%d9%87-%da%86%db%8c%d8%b2-%d8%af%d8%b1-%d9%85%d9%88%d8%b1%d8%af-%d9%81%d9%88%d9%85-%d9%be%d8%a7%d8%b1%da%a9%d8%aa-%d9%84%d9%85%db%8c%d9%86%d8%aa/" target="_blank">فوم</a>&nbsp;معمولی نور را از خود عبور میدهد و اگر دستتان را در پشت آن قرار دهید از سمت دیگر کاملا قابل مشاهده است، اما فوم سایلنت اینگونه نیست و نور را از خود عبور نمی دهد<span dir="LTR">.</span></p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-11.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-12.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	ضخامت استاندارد فوم برای لمینت</p>
<p dir="RTL" style="text-align: justify;">
	در هنگام انتخاب فوم به ضخامت آن نیز توجه ویژه داشته باشید که کمتر یا بیشتر از 2 میلیمتر نباشد. همچنین این حساسیت را در هنگام خرید فوم معمولی یا سایلنت نیز به خرج دهید، چرا که متاسفانه گاهی مواقع فوم کفی کفش یا فوم عایق کاری موتورخانه، به جای فوم مخصوص لمینت به مشتریان ارائه می شود و باعث خرابی لمینتها می گردد.</p>
<p dir="RTL" style="text-align: justify;">
	استفاده از فومهای غیر استاندارد تاثیر مخرب خود را روی لمینتها از همان ابتدا میگذارد. رنگ فوم ها حتما سفید رنگ باشند زیرا فومهای رنگ مشکی یا سایر رنگها، از مواد بازیافتی بوده و علاوه بر آلوده کردن محیط، از مقاومت کافی نیز برخوردار نیستند. البته موضوع رنگ در مورد لمینت های پشت فوم دار صدق نمی کند. معمولاً پارکت لمینت های پشت فومدار، دارای فومهای رنگی مانند نارنجی، سبز یا خاکستری هستند.</p>
<p dir="RTL" style="text-align: justify;">
	قبل از نصب لمینت فراهم کردن یک سطح کاملا مسطح و صیقلی با کمترین میزان اختلاف سطح برای نصب لمینت ها لازم و ضروری است. فاصله بین کمترین و بیشترین اختلاف سطح نباید از 4 میلیمتر در هر متر مربع بیشتر باشد.</p>
<p dir="RTL" style="text-align: justify;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	لمینت های فوم دار</p>
<p dir="RTL" style="text-align: justify;">
	برخی از لمینت ها خودشان دارای فوم هستند. یعنی فوم به پشت آنها چسبیده شده است. دقت کنید در بعضی از برندها فوم چسبانده شده توسط کارخانه، به خوبی بریده نشده و نسبت به ابعاد لمینت، بزرگتر یا کوچکتر است. اگرچه اکثر فروشندگان اظهار می کنند که اهمیت چندانی نخواهد داشت. اما این مسئله در هر دو حالت دردسرساز خواهد بود.</p>
<p dir="RTL" style="text-align: center;">
	<img alt="نکاتی در خرید پارکت لمینت" class="e-w" src="http://decotook.com/papers/files/pictures/tips-on-parquets/tips-on-parquets-13.jpg" /></p>
<p dir="RTL" style="text-align: justify;">
	قیمت فوم سایلنت و فوم معمولی</p>
<p dir="RTL" style="text-align: justify;">
	قیمت فوم معمولی در حدود هر متر مربع هزار تومان و فوم سایلنت هر متر مربع سه تا سه هزار و پانصد تومان می باشد</p>
<p dir="RTL" style="text-align: justify;">
	<strong>ضریب سایشی لمینت</strong></p>
<p dir="RTL" style="text-align: justify;">
	لمینت ها از نظر استاندارد، دارای رده بندی<span dir="LTR"> AC </span>می باشند. هر چه عدد <span dir="LTR">AC</span> بزرگتر باشد، ضریب سایشی لمینت بیشتر بوده و در مقابل خط و خش مقاوم تر است. استاندارد<span dir="LTR"> AC/3 </span>برای مصارف مسکونی استفاده می شود و<span dir="LTR"> AC/4 </span>و بالاتر برای مکانهای اداری و پرتردد مانند فروشگاهها و ادارات مورد استفاده قرار می گیرد. توجه داشته باشید که<span dir="LTR"> AC </span>بالاتر همیشه نشان دهنده بهتر بودن کالا نیست. بعبارت دیگر پارکت لمینت با<span dir="LTR"> AC/4 </span>از پارکت لمینت دیگری که<span dir="LTR"> AC/3 </span>&nbsp;دارد لزوما بهتر نیست. زیرا ممکن است شما لمینتی انتخاب کنید که بالاترین استاندارد موجود در بازار را داشته باشد ولی از نظر کلیک بسیار ضعیف باشد و پس از مدتی کلیکها از یکدیگر باز شوند و یا بشکنند.</p>
<p dir="RTL" style="text-align: justify;">
	نقش پارافین بر کلیک های لمینت و علت سر و صدای پارکت ها</p>
<p dir="RTL" style="text-align: justify;">
	لمینت هایی خوب هستند که علاوه بر استحکام بسیار بالا و چفت و بست خوب، به پارافین نیز آغشته شده است تا علاوه بر نفوذ کمتر آب، در هنگام تردد نیز به علت خاصیت روغنی و چرب پارافین، کمتر ایجاد صدا کند. البته همانطور که قبلاً نیز گفته شد یکی دیگر از علت های بوجود آمدن صدای پارکت نیز وجود اختلاف سطح در سطح زیرین قبل از نصب لمینت ها است. اختلاف سطح در هر متر مربع نباید بیشتر از 4 میلیمتر باشد.&nbsp;</p>
