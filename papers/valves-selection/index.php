<p dir="RTL" style="text-align: justify;">
	<strong>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>جنس شیرآلات بهداشتی:</strong></p>
<p dir="RTL" style="text-align: justify;">
	بطور کلی شیرآلات بهداشتی دارای دو بخش عمده یعنی پوسته داخلی (پایه اصلی شیر) و پوشش خارجی هستند که در بخش داخلی تولیدکنندگان با توجه به سفارهایی که دارند معمولا از آلیاژ برنج یا سرب خشک استفاده می کنند این در حالی است که رعایت مسایل بهداشتی باعث شده تا اکثر تولید کنندگان امروزه از خیر سرب خشک گذشته و بیشتر به استفاده از آلیاژ برنج فکر کنند چون سرب در صورتی که آبکاری شیر به خوبی انجام نشود در مقابل آب به مرور زمان محلول می شود و آلودگی های خطرناکی را برای افراد به ارمغان می آورد.</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="خرید شیرآلات بهداشتی" class="e-w" src="http://decotook.com/papers/files/pictures/valves-selection/valves-selection-2.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	در بخش خارجی یا همان پوشش خارجی شیرهای ساختمانی که توجه اکثر ما را به خود جلب می کند معمولا کارخانه ها از فلزاتی چون برنج، برنز، مس و یا استیل استفاده می کنند که معمول ترین نوع آبکاری استیل است ودیگر فلزات برای سفارش های خاص و منحصربفرد استفاده می شوند . اما بهترین نوع آبکاری در بازار به <span dir="LTR">PVD</span> معروف است در این نوع آبکاری شرکت تولیدکننده، بخش خارجی شیرها را به وسیله اتم های باردار فلز مورد نظرش مثل کرم، برنز یا هر فلز دیگری که سفارش داده شده باشد، بمباران می کند تا به طور شیمیایی سطح فلز پایه (فلز بدنه شیرها) با فلز مورد نظر پوشیده شود که به آن فرایند رسوب سازی با بخار می گویند. با این روش علاوه بر افزایش طول عمر شیر، کیفیت آن در مقابل ساییدگی، خوردگی، زنگ زدگی و ده ها مورد دیگرتضمین می شود.&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="خرید شیرآلات بهداشتی" class="e-w" src="http://decotook.com/papers/files/pictures/valves-selection/valves-selection-3.jpg" style="width: 550px; height: 420px;" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	بیشتر شیرها برای باز و بسته کردن جریان آب از ابزاری به نام کارتریج یا مغزی استفاده می کنند. کارتریج های سرامیکی بهترین نوع کارتریج هستند.</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="خرید شیرآلات بهداشتی" class="e-w" src="http://decotook.com/papers/files/pictures/valves-selection/valves-selection-4.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	همچنین از دیگر نشانه های با کیفیت بودن یک شیر، وزن آن است. اگر درباره کیفیت شیر تردید دارید آنرا در دست بگیرید و وزن آن را با شیرهای دیگر مقایسه کنید. شیرهایی که دارای بدنه برنجی ضخیم تری هستند سنگین ترند واز دوام بالاتری برخوردار هستند و نیاز به مراقبت کمتری دارند.</p>
<p dir="RTL" style="text-align: center;">
