<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	<strong>2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>شکل مناسب شیر دستشویی و روشویی و انواع آن:</strong></p>
<p dir="RTL" style="text-align: justify;">
	برای جاهایی که فضای کوچکی دارند بهتر است از مدل هایی از شیرآلات استفاده شود که در داخل دیوار قرار می گیرند. این نوع شیرآلات برای صرفه جویی در فضا بسیار مناسب بوده و مشکل تمیز کردن لکه های کثیفی یا رسوبات آب را هم ندارند. همچنین می توانید از دوش و شیر آلات دیواری برای حمامتان هم استفاده کنید.</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="خرید شیرآلات بهداشتی" class="e-w" src="http://decotook.com/papers/files/pictures/valves-selection/valves-selection-6.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	امروزه محبوب ترین شیرهای مورد استفاده شیرهای اهرمی هستند. استفاده ازشیرهای اهرمی بسیار ساده است. شما می توانید تنها با حرکت دادن اهرم به سمت بالا جریان آب را برقرار کنید، با پایین فشردن آن جریان آب را قطع کنید و با چپ و راست کردن آن نیز می توانید میزان دمای اب را نیز تنظیم نمایید.</p>
<p dir="RTL" style="text-align: justify;">
	این شیر آلات در دونوع پایه کوتاه و پایه بلند تولید می شود که شما می توانید متناسب با طراحی خود یکی از آن ها را انتخاب نمایید. معمولا از شیر آلات اهرمی برای سرویس های بهداشتی و حمام استفاده می کنند اما مدل های اهرمی برای آشپزخانه نیز وجود دارد.</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="خرید شیرآلات بهداشتی" class="e-w" src="http://decotook.com/papers/files/pictures/valves-selection/valves-selection-7.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	نوع دیگر شیرآلات مورد استفاده شیرآلات هوشمند هستند. مزیت استفاده از این شیرآلات کمک به صرفه جویی در مصرف آب و بهداشت عمومی است به طوریکه نصب نمونه های خانگی شیرهای هوشمند در خانه ها موجب کاهش ۷۰ درصدی مصرف آب شده که این میزان در اماکن عمومی به ده برابر نیز رسیده است. افزایش بهداشت عمومی به دلیل عدم تماس مستقیم دست با شیر از دیگر ویژگی های استفاده از شیرهای هوشمند محسوب می شود.</p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: center;">
	<img alt="خرید شیرآلات بهداشتی" class="e-w" src="http://decotook.com/papers/files/pictures/valves-selection/valves-selection-8.jpg" /></p>
<p dir="RTL" style="text-align: center;">
	&nbsp;</p>
<p dir="RTL" style="text-align: justify;">
	<strong>3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>رنگ های زیبا</strong></p>
<p dir="RTL" style="text-align: justify;">
	کروم ممکن است که یکی از بهترین پوشش ها برای شیرآلات باشد اما تنها راه و تنها انتخاب شما نیست. اگر به دنبال لوکس بودن و تجملات هستید، حتی می توانید از شیرآلاتی که با طلا پوشانده شده اند، استفاده کنید و اگر بدنبال جنس مقاوم تر و خاص هستید، ما به شما شیرآلات پوشانده شده با نیکل را پیشنهاد می کنیم. اگر به هر دلیلی نمی خواهید برای شیرآلات هزینه ی اضافی بابت لوکس و زیبا بودن بپردازید، می توانید از شیرآلات با رنگ های شاد استفاده کنید که اخیرا بسیار محبوب و در دسترس هستند.</p>
<p dir="RTL" style="text-align: center;">
